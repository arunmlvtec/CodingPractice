	public ArrayList<ArrayList<Integer>> setZeroes(ArrayList<ArrayList<Integer>> a) {
		List<Integer> rowList = new ArrayList<Integer>();
		List<Integer> colList = new ArrayList<Integer>();
		int rowLen = a.size(), colLen = a.get(0).size();
		// System.out.println("Row Length is: "+rowLen+" Column Length is: "+colLen);
		
		for (int i = 0; i < rowLen; i++) {
			for (int j = 0; j < colLen; j++) {
				if (a.get(i).get(j) == 0) {
					if (!rowList.contains(i))
						rowList.add(i);
					if (!colList.contains(j))
						colList.add(j);
				}
			}
		}
		
		for (Integer i : rowList)
			for (int j = 0; j < rowLen; j++)
				a.get(i).set(j, 0);
		
		for (Integer j : colList)
			for (int i = 0; i < colLen; i++)
				a.get(i).set(j, 0);
		return a;
	}
	
